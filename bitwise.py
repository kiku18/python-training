a=5
b=10
print(a)
print(b)
# note left shift increases value whereas the right shift decreases the value.
print(f"The left shift of {a} by 1 space is", a<<1)
print(f"The left shift of {a} by 2 space is", a<<2)
print(f"The left shift of {a} by 3 space is", a<<3)
print(f"The right shift of {b} by 1 space is", b>>1)
print(f"The right shift of {b} by 1 space is", b>>2)
